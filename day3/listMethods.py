#__________****list.append() #adds one element at the end*****______________
list = [2, 1, 3]
list.append(4)
print(list)

#___________****list.sort() #sort in ascending order****_________
list = [2, 1, 3]
print(list.append(4))
print(list.sort())
print(list)

#____________****list.sorts(reverse = True) in descending order****_____________
list = [2, 1, 3]
print(list.sort(reverse = True))
print(list)

#_____________***ascending order***____________
list = [2, 1, 3]
print(list.sort())
print(list)

#________****list.reverse() #reverses list***________
list = ['a', 'b', 'c', 'd', 'e', 'f']
list.reverse()
print(list)

#____________****insert(idx,el) #insert element at index
list = ['a', 'b', 'c', 'd', 'e', 'f']
list.insert(3,4 )
print(list)

#__________****list.remove(1) #removes first occurrence of element****___________
list = [1, 2, 3, 4, 5, 6, ]
list.remove(3)
print(list)

#_________****list.pop(idx) #removes element at idx****___________
list = [2, 3, 4, 5, 6]
list.pop(0)
print(list)




